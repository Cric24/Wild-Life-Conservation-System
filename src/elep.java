    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            String hab=txthabit.getText();
            String elep=txtelep.getText();
            String year=txtyear.getText();
            String wpa=txtwpa.getText();
            String mig=txtmig.getText();
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/wildlife_conservation_management","root","");
            PreparedStatement pa=con.prepareStatement("insert into elephant(Habitat,no_of_elep,year,wpa,migration,gender) values(?,?,?,?,?,?)");
            pa.setString(1, hab);
            pa.setString(2, elep);
            pa.setString(3, year);
            pa.setString(4, wpa);
            pa.setString(5, mig);
            
            
            if(jRadioButton1.isSelected())
            {
                pa.setString(6,jRadioButton1.getText());
            }else
            pa.setString(6,jRadioButton2.getText());
            pa.executeUpdate();
            JOptionPane.showMessageDialog(this,"Inserted Successfully");

        } catch (Exception ex) {
            Logger.getLogger(elep.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

